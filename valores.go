package main

import "fmt"

func valores() {

	fmt.Println("go" + "lang") //concatenar dos cadenas, se usa el +

	fmt.Println("1+1 =", 1+1) // concatenar una cadena con un valor resultado de una operacion, se usa ,

	fmt.Println(true && false) //compuerta AND, opera dos booleanos

	fmt.Println(true || false) //compuerta OR, opera dos booleanos

	fmt.Println(!true) //negacion logica, niega el booleano

}
